package com.gaetan.virus;

public final class Guys {
    private final int x;
    private final int y;

    public Guys(final int x, final int y) {
        this.x = x;
        this.y = y;
    }

    public int getY() {
        return this.y;
    }

    public int getX() {
        return this.x;
    }
}
